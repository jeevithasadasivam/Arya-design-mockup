    }
    
    class var gradientOrange: UIColor {
        return .init(red: 243/255, green: 130/255, blue: 132/255, alpha: 1)
    }
    
    class var gradientGreen: UIColor {
        return .init(red: 66/255, green: 227/255, blue: 151/255, alpha: 1)
    }
    
    class var gradientBeach: UIColor {
        return .init(red: 53/255, green: 171/255, blue: 174/255, alpha: 1)
    }
    
    class var gradientPurple: UIColor {
